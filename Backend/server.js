app.post('/login', (req, res) => {
  query.login(req.body)
    .then(response => {
      res.status(200).send(response);
    })
    .catch(error => {
      res.status(500).send(error);
    })
})

//-------------------------------------------------------------------------------------------------------------------------------------------------------

